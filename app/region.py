def parse_uscc(uscc: str) -> dict[str, str]:
    """解析统一社会信用代码，返回 {'province': '...', 'city': '...', 'region_code': '420112'}。

    失败时 province/city 留空（调用方可让 LLM 兜底或用户补录）。
    """
    uscc = (uscc or "").strip().upper()
    if len(uscc) != 18:
        return {"province": "", "city": "", "region_code": ""}
    code = uscc[2:8]
    if not code.isdigit():
        return {"province": "", "city": "", "region_code": ""}
    province = PROVINCES.get(code[:2], "")
    city = CITIES.get(code[:4], "")
    return {"province": province, "city": city, "region_code": code}


# ---------------------------------------------------------------------------
# GB 32100-2015 统一社会信用代码校验
# ---------------------------------------------------------------------------

# 字符集：去掉容易混淆的 I O S V Z，共 31 个字符
USCC_CHARSET = "0123456789ABCDEFGHJKLMNPQRTUWXY"
USCC_WEIGHTS = [1, 3, 9, 27, 19, 26, 16, 17, 20, 29, 25, 13, 8, 24, 10, 30, 28]
# 整体形态校验：[0:2] 登记管理部门+机构类别（含字母），[2:8] 行政区划数字，[8:18] 主体标识码（含字母）
USCC_RE = re.compile(r"^[0-9A-HJ-NPQRTUWXY]{2}\d{6}[0-9A-HJ-NPQRTUWXY]{10}$")


def validate_uscc(code: str) -> tuple[bool, str]:
    """校验统一社会信用代码（GB 32100-2015）。

    返回 (是否合法, 错误原因)。合法时 reason 为空字符串。
    """
    if not isinstance(code, str):
        return False, "必须是字符串"
    code = code.strip().upper()
    if len(code) != 18:
        return False, "长度必须为 18 位"
    if not USCC_RE.match(code):
        return False, "格式不合法（含非法字符或行政区划位非数字）"
    try:
        s = sum(USCC_CHARSET.index(c) * w for c, w in zip(code[:17], USCC_WEIGHTS))
    except ValueError:
        return False, "包含非法字符（不允许 I O S V Z）"
    expected = USCC_CHARSET[(31 - s % 31) % 31]
    if code[17] != expected:
        return False, f"第 18 位校验码错误，应为 {expected}"
    return True, ""


# ---------------------------------------------------------------------------
# GB 11643-1999 居民身份证号码校验（18 位）
# ---------------------------------------------------------------------------

_ID_CARD_WEIGHTS = [7, 9, 10, 5, 8, 4, 2, 1, 6, 3, 7, 9, 10, 5, 8, 4, 2]
_ID_CARD_CHECK = "10X98765432"
_ID_CARD_RE = re.compile(r"^\d{17}[\dX]$")


def validate_id_card(code: str) -> tuple[bool, str]:
    """校验居民身份证号码（GB 11643-1999，18 位）。"""
    if not isinstance(code, str):
        return False, "必须是字符串"
    code = code.strip().upper()
    if len(code) != 18:
        return False, "长度必须为 18 位"
    if not _ID_CARD_RE.match(code):
        return False, "格式不合法（前 17 位必须是数字，第 18 位为数字或 X）"
    # 出生日期粗校验
    yyyy = int(code[6:10])
    mm = int(code[10:12])
    dd = int(code[12:14])
    if not (1900 <= yyyy <= 2099 and 1 <= mm <= 12 and 1 <= dd <= 31):
        return False, "出生日期段不合法"
    # 行政区划前 2 位必须命中省级代码
    if code[:2] not in PROVINCES:
        return False, "省级代码不合法"
    s = sum(int(c) * w for c, w in zip(code[:17], _ID_CARD_WEIGHTS))
    expected = _ID_CARD_CHECK[s % 11]
    if code[17] != expected:
        return False, f"第 18 位校验码错误，应为 {expected}"
    return True, ""


def parse_id_card(code: str) -> dict[str, str]:
    """从身份证号解析省份/城市（前 6 位行政区划，复用 GB/T 2260）。"""
    code = (code or "").strip().upper()
    if len(code) != 18 or not code[:6].isdigit():
        return {"province": "", "city": "", "region_code": ""}
    region = code[:6]
    return {
        "province": PROVINCES.get(region[:2], ""),
        "city": CITIES.get(region[:4], ""),
        "region_code": region,
    }
